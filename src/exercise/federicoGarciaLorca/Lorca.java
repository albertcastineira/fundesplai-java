package exercise.federicoGarciaLorca;

public class Lorca {

    private String poem =
        "Córdoba.\n" +
        "Lejana y sola.\n" +
        "\n" +
        "Jaca negra, luna grande,\n" +
        "y aceitunas en mi alforja.\n" +
        "Aunque sepa los caminos\n" +
        "yo nunca llegaré a Córdoba.\n" +
        "\n" +
        "Por el llano, por el viento,\n" +
        "jaca negra, luna roja.\n" +
        "La muerte me está mirando\n" +
        "desde las torres de Córdoba.\n" +
        "\n" +
        "¡Ay qué camino tan largo!\n" +
        "¡Ay mi jaca valerosa!\n" +
        "¡Ay que la muerte me espera,\n" +
        "antes de llegar a Córdoba!";
    public void printPoem() {
        System.out.println(poem);
    }
}
